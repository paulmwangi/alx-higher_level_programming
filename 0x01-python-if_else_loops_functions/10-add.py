#!/usr/bin/python3

def add(a, b):
    """
    Adds two integers and returns the result.

    Args:
    - a: integer
    - b: integer

    Returns:
    - The sum of a and b
    """
    return a + b


if __name__ == "__main__":
    print(add(1, 2))      # Output: 3
    print(add(98, 0))     # Output: 98
    print(add(100, -2))   # Output: 98
